<script type="text/javascript">
    window.addEventListener ('load', function () {
        $("#btn-ingresar").click(function(e) {
            if ($("#usuario").val().trim() == '') {
                $("#usuario").focus();
                Swal.fire({
                    icon: 'error',
                    title: 'Error',
                    text: 'Complete el usuario.'
                });

                e.preventDefault();
                
                return;
            }

            if ($("#password").val().trim() == '') {
                $("#password").focus();
                Swal.fire({
                    icon: 'error',
                    title: 'Error',
                    text: 'Complete la clave.'
                });

                e.preventDefault();
                
                return;
            }
        });
    })
</script>
